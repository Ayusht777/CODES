//POINTER BASIC -01
#include<stdio.h>
int main()
{
        //Address in C
        // it means memory location of the variable
        int x =5;
        printf("THE VALUE OF X=%d",x);
        printf("\nTHE ADDRESS OF X=%p",&x);//here &x is giving the address of x
       



         // %p is used to print the address of this pointer
         // and every time you run it will give you different address

}
